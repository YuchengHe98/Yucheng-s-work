class UserInfo{
    constructor (name, username, password, email, age, gender, birthday, address) {
        this.name = name;
        this.username = username;
        this.password = password;
        this.email = email;
        this.age = age;
        this.gender = gender;
        this.birthday = birthday;
        this.address = address;
    }
}

